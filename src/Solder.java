
public class Solder {
	
// Attributes 
	public String nom;
	public Price price;
	public int points_vie;
	public int puissance_attaque;
	public int catacite_defence;
	
// Getter / Setter Methods 
	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public Price getPrix() {
		return price;
	}

	public void setPrix(Price price) {
		this.price = price;
	}

	public int getPoints_vie() {
		return points_vie;
	}

	public void setPoints_vie(int points_vie) {
		this.points_vie = points_vie;
	}

	public int getPuissance_attaque() {
		return puissance_attaque;
	}

	public void setPuissance_attaque(int puissance_attaque) {
		this.puissance_attaque = puissance_attaque;
	}

	public int getCatacite_defence() {
		return catacite_defence;
	}

	public void setCatacite_defence(int catacite_defence) {
		this.catacite_defence = catacite_defence;
	}
	

}
